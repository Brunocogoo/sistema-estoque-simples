    private void butCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butCadastroActionPerformed
        if(jTextFieldNome.getText().length()>0 && jTextFieldEndereco.getText().length()>0 &&
                jFormattedTextFieldCnpj.getText().trim().length()==18 && jFormattedTextFieldTelefone.getText().trim().length()==13){
                String query = "UPDATE Fornecedor set nome='"+jTextFieldNome.getText()+
                "',endereco='"+jTextFieldEndereco.getText()+
                "',cnpj='"+jFormattedTextFieldCnpj.getText()+
                "',telefone='"+jFormattedTextFieldTelefone.getText()+
                "' WHERE nome='"+nomeAntigo+"' AND endereco='"+enderecoAntigo+
                "' AND cnpj='"+cnpjAntigo+"' AND telefone='"+telefoneAntigo+"'";
        try{
            statement.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Fornecedor editado com sucesso");
            
        }catch(Exception e){
            System.out.println("Erro ao atualizar");
            System.out.println(e.getMessage());
        }
        }else JOptionPane.showMessageDialog(this, "ERRO. Algum campo esta faltando");
        this.dispose();
    }//GEN-LAST:event_butCadastroActionPerformed

